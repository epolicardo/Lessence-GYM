    private void jBListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBListarActionPerformed
        List<Actividades_Socios_Turnos> listaActividades_Socios_Turnos = Actividades_Socios_Turnos.listaActividades_Socios_Turnos();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        for (int j = 0; j < listaActividades_Socios_Turnos.size(); j++) {
            Actividades_Socios_Turnos ast = listaActividades_Socios_Turnos.get(j);
            if (ast.getRelacion().getProfesor().getPersona().getDNI().equals(jtDNI.getText())) {
                Object[] fila = {ast.getSocio().getPersona().getApellido() + ", " + ast.getSocio().getPersona().getNombre(),
                    ast.getRelacion().getActividad().getActividad(),
                    ast.getRelacion().getHorario().getHorario(),
                    ast.getSocio().getEstado().getEstado()};
                modelo.addRow(fila);
                jtProfesor.setText(ast.getRelacion().getProfesor().getPersona().getNombre() + " " + ast.getRelacion().getProfesor().getPersona().getApellido());
            }
        }
    }//GEN-LAST:event_jBListarActionPerformed

