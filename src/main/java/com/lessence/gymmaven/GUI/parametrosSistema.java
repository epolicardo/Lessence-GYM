    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        ParametrosSistema.setNombreEmpresa(jtNombreEmpresa.getText());
        ParametrosSistema.setImagenEmpresa(jtimagenEmpresa.getText());
        ParametrosSistema.setMysqldump(jtMySQLDump.getText());
        ParametrosSistema.setDiasVigencia(Integer.parseInt(jsDiasVigencia.getValue().toString()));
        ParametrosSistema.setAvisoVencimientos((Integer)jsProximosVencimientos.getValue());
        ParametrosSistema.setHorasAsistencias(Integer.parseInt(jsUltimosAccesos.getValue().toString()));
        ParametrosSistema.setEquipoDB(jtEquipoDB.getText());
        ParametrosSistema.setPassAdmin(String.valueOf(jpClaveAdministrador.getPassword()));
        
        ParametrosSistema.GuardaParametros();
        JOptionPane.showMessageDialog(this, "Los par치metros del sistema fueron actualizados correctamente", "Parametros de Sistema", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jbGuardarActionPerformed

