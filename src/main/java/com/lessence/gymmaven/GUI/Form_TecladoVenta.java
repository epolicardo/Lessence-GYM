    private void jbGrabarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGrabarVentaActionPerformed

        if (!jtImporte.getText().equals("")) {
            if (!jtConcepto.getText().equals("")) {
                float Importe = Float.valueOf(jtImporte.getText());
                String Concepto = jtConcepto.getText();
                Session sesion = HibernateUtil.getSessionFactory().openSession();
                Query consulta = sesion.createQuery("FROM Caja_diaria");
                List<Caja_diaria> caja = consulta.list();
                int ultimo_registro = caja.size();
                float saldo;
                if (caja.size() == 0) {
                    saldo = 0;
                } else {
                    saldo = caja.get(ultimo_registro - 1).getSaldo();
                }
                sesion.beginTransaction();
                Caja_diaria registro = new Caja_diaria();
                FormasPago formaPago = new FormasPago();
                formaPago.setIdFormaPago(1); //Gastos se realizan desde el efectivo
                registro.setFecha(Date.from(Instant.now()));
                registro.setFormaPago(formaPago);
                registro.setCategoria(6); // 2 - Ingresos Manuales / 3 - Retiro Manual  // 4 - Gastos // 6-Ventas
                registro.setConcepto(Concepto);
                registro.setHaber(Importe);
                registro.setSaldo(saldo + Importe);
                sesion.save(registro);
                sesion.getTransaction().commit();
                sesion.close();
                ParametrosSistema.cantidadMovimientos = ParametrosSistema.cantidadMovimientos + 1;
                ParametrosSistema.GuardaParametros();
                JOptionPane.showMessageDialog(this, "Venta grabada con Ã©xito");
                jtConcepto.setText("");
                jtImporte.setText("");
                jtImporte.requestFocus();
                getRootPane().setDefaultButton(jbGrabarVenta);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar un concepto");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un importe");
        }


    }//GEN-LAST:event_jbGrabarVentaActionPerformed

