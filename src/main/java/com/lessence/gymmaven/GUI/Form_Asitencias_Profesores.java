    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed

        if (106 == evt.getKeyCode()) {
            jTextField1.setText("");
        } else if (10 == evt.getKeyCode()) {
            Session sesion = HibernateUtil.getSessionFactory().openSession();
            sesion.flush();
            Criteria consultaProfesores = sesion.createCriteria(Profesores.class);
            List<Profesores> listaProfesores = consultaProfesores.list();
            sesion.beginTransaction();
            for (int i = 0; i < listaProfesores.size(); i++) {
                Profesores profesor = listaProfesores.get(i);
                if (profesor.getPersona().getDNI().equals(jTextField1.getText())) {
                    jLabel2.setText("Hola " + profesor.getPersona().getNombre());
                    RegistroAsistenciasProfesores asistencia = new RegistroAsistenciasProfesores();
                    asistencia.setFecha(Date.from(Instant.now()));
                    asistencia.setProfesor(profesor);
                    sesion.save(asistencia);
                    sesion.getTransaction().commit();
                }
            }
            sesion.close();
            JOptionPane.showMessageDialog(this, "Se registro la asistencia",
                    "Gestion de Profesores", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.setText("");
            this.dispose();
        }
    }//GEN-LAST:event_jTextField1KeyPressed

