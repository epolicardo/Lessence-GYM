    private void ProfeSeleccionado(String DNI) {

        int i = 0;
        int j = 0;
        int k = 0;
        int l = 0;
        DefaultTableModel TablaActividades = (DefaultTableModel) jTActividades.getModel();
        TablaActividades.setRowCount(i);
        DefaultTableModel TablaCuenta = (DefaultTableModel) jTable1.getModel();
        TablaCuenta.setRowCount(k);

        Session sesion = HibernateUtil.getSessionFactory().openSession();
        Criteria consulta = sesion.createCriteria(RegistroAsistenciasProfesores.class);
        List<RegistroAsistenciasProfesores> asistencias = consulta.list();
        for (int m = 0; m < asistencias.size(); m++) {
            RegistroAsistenciasProfesores registro = asistencias.get(m);
            String DNI_Obtenido = registro.getProfesor().getPersona().getDNI();
            if (DNI_Obtenido.equals(DNI)) {
                String tiempoRegistro = new SimpleDateFormat("dd-MM-yyyy - HH:mm:ss").format(registro.getFecha());

                Object[] fila = {tiempoRegistro};
                TablaCuenta.addRow(fila);
            }
        }
        Criteria consultaRelacion = sesion.createCriteria(Relaciones.class);
        List<Relaciones> listaRelaciones = consultaRelacion.list();
        for (int h = 0; h < listaRelaciones.size(); h++) {
            Relaciones relacion = listaRelaciones.get(h);
            if (relacion.getProfesor().getPersona().getDNI().equals(DNI)) {
                Object[] fila = {relacion.getActividad().getActividad(),
                    relacion.getHorario().getHorario()};
                TablaActividades.addRow(fila);
            }
        }
        jBActividades.setEnabled(true);
        jbConsultaAlumnos.setEnabled(true);

    }


