    private void ActualizarGrillaProfesores() {

        Session sesion = HibernateUtil.getSessionFactory().openSession();
        DefaultTableModel modelo = (DefaultTableModel) jTablaProfesores.getModel();
        modelo.setRowCount(0);

        Criteria query_profesores = sesion.createCriteria(Profesores.class);
        List<Profesores> lista_profesores = query_profesores.list();
        for (int i = 0; i < lista_profesores.size(); i++) {
            if (lista_profesores.isEmpty()) {

            } else {
                Profesores profesor = lista_profesores.get(i);
                String DNI = profesor.getPersona().getDNI();
                String Profesor = profesor.getPersona().getApellido() + " "
                        + profesor.getPersona().getNombre();
                String Celular = profesor.getPersona().getCelular();
                String Correo = profesor.getPersona().getCorreo();
                Object[] fila = {DNI, Profesor, Celular, Correo};
                modelo.addRow(fila);
            }

        }
        sesion.close();
    }
